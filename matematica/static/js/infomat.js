$(function() {

	$("#extend_add").hide();
	$("#extend_sub").hide();
	$("#extend_money").hide();
	$("#extend_mcdu").hide();
	
	$("#add").click(function(event) {	
		$("#extend_add").show();
	});
	
	$("#sub").click(function(event) {	
		$("#extend_sub").show();
	});
	
	$("#money").click(function(event) {	
		$("#extend_money").show();
	});
	
	$("#mcdu").click(function(event) {	
		$("#extend_mcdu").show();
	});
	
});